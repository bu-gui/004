#define SSD1306_ADDR            0x3C
#define SSD1306_WIDTH           128
#define SSD1306_HEIGHT          64
#define SSD1306_PAGES           (SSD1306_HEIGHT / 8)

static uint8_t framebuffer[SSD1306_WIDTH * SSD1306_PAGES];

static void write_cmd(uint8_t cmd)
{
    i2c_cmd_handle_t h = i2c_cmd_link_create();
    i2c_master_start(h);
    i2c_master_write_byte(h, (SSD1306_ADDR << 1) | I2C_MASTER_WRITE, 1);
    i2c_master_write_byte(h, 0x00, 1);
    i2c_master_write_byte(h, cmd, 1);
    i2c_master_stop(h);
    i2c_master_cmd_begin(I2C_NUM_0, h, pdMS_TO_TICKS(100));
    i2c_cmd_link_delete(h);
}

static void write_data(uint8_t data)
{
    i2c_cmd_handle_t h = i2c_cmd_link_create();
    i2c_master_start(h);
    i2c_master_write_byte(h, (SSD1306_ADDR << 1) | I2C_MASTER_WRITE, 1);
    i2c_master_write_byte(h, 0x40, 1);
    i2c_master_write_byte(h, data, 1);
    i2c_master_stop(h);
    i2c_master_cmd_begin(I2C_NUM_0, h, pdMS_TO_TICKS(100));
    i2c_cmd_link_delete(h);
}

void ssd1306_init(void)
{
    vTaskDelay(pdMS_TO_TICKS(100));
    write_cmd(0xAE);
    write_cmd(0xD5);
    write_cmd(0x80);
    write_cmd(0xA8);
    write_cmd(0x3F);
    write_cmd(0xD3);
    write_cmd(0x00);
    write_cmd(0x40);
    write_cmd(0x8D);
    write_cmd(0x14);
    write_cmd(0x20);
    write_cmd(0x00);
    write_cmd(0xA1);
    write_cmd(0xC8);
    write_cmd(0xDA);
    write_cmd(0x12);
    write_cmd(0x81);
    write_cmd(0xCF);
    write_cmd(0xD9);
    write_cmd(0xF1);
    write_cmd(0xDB);
    write_cmd(0x40);
    write_cmd(0xA4);
    write_cmd(0xA6);
    write_cmd(0xAF);
}

void ssd1306_clear(void)
{
    memset(framebuffer, 0, sizeof(framebuffer));
}

void ssd1306_draw_pixel(int x, int y, int color)
{
    if (x < 0 || x >= SSD1306_WIDTH || y < 0 || y >= SSD1306_HEIGHT) return;
    if (color) {
        framebuffer[x + (y / 8) * SSD1306_WIDTH] |= (1 << (y % 8));
    } else {
        framebuffer[x + (y / 8) * SSD1306_WIDTH] &= ~(1 << (y % 8));
    }
}

void ssd1306_display(void)
{
    for (int page = 0; page < SSD1306_PAGES; page++) {
        write_cmd(0xB0 + page);
        write_cmd(0x00);
        write_cmd(0x10);
        i2c_cmd_handle_t h = i2c_cmd_link_create();
        i2c_master_start(h);
        i2c_master_write_byte(h, (SSD1306_ADDR << 1) | I2C_MASTER_WRITE, 1);
        i2c_master_write_byte(h, 0x40, 1);
        i2c_master_write(h, &framebuffer[page * SSD1306_WIDTH], SSD1306_WIDTH, 1);
        i2c_master_stop(h);
        i2c_master_cmd_begin(I2C_NUM_0, h, pdMS_TO_TICKS(100));
        i2c_cmd_link_delete(h);
    }
}

